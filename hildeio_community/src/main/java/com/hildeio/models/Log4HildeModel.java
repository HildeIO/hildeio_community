package com.hildeio.models;

import java.sql.Timestamp;

import jakarta.persistence.*;
import lombok.Getter;
import lombok.Setter;

/***********************************************************************************************
 * Model Log4HildeModel
 ***********************************************************************************************/
@Setter
@Getter
@Entity
@Table(name = "log4hilde")
public class Log4HildeModel {
	
	/***********************************************************************************************
	 * eindeutige Id  in der Tabelle [luidi_db].[log4hilde]
	 ***********************************************************************************************/
	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	private Long id;
	
	/***********************************************************************************************
	 * Zeitstempel des Loggingeintrags
	 ***********************************************************************************************/
	private Timestamp logtime;

	/***********************************************************************************************
	 * Klasse in der Loggingeintrag erzeugt wurde
	 ***********************************************************************************************/
	private String klasse;
	
	/***********************************************************************************************
	 * Methode in der Loggingeintrag erzeugt wurde
	 ***********************************************************************************************/
	private String methode;
	
	/***********************************************************************************************
	 * Logpoint in der Loggingeintrag erzeugt wurde
	 ***********************************************************************************************/
	private String logpoint;
	
	/***********************************************************************************************
	 * Kategorie des Loggingeintrags (INFO / ERROR)
	 ***********************************************************************************************/
	@Enumerated(EnumType.STRING)
	private Kategorie kategorie;
	
	/***********************************************************************************************
	 * Aktuelle WorkflowId
	 ***********************************************************************************************/
	@Column(length = 50)
	private String eventId;

	/***********************************************************************************************
	 * Logtext / Errortext
	 ***********************************************************************************************/
	@Column(length = 80000)
	private String meldung;
	
	/***********************************************************************************************
	 * Errortext
	 ***********************************************************************************************/
	private String exception;
	
	/***********************************************************************************************
	 * Vollstaendige Exception
	 ***********************************************************************************************/
	private String stackTrace;
		
	/***********************************************************************************************
	 * zusaetzliche Informationen
	 ***********************************************************************************************/
	@Column(length = 80000)
	private String objekte;
	
	/***********************************************************************************************
	 * Kategorie (INFO / ERROR)
	 ***********************************************************************************************/
	public enum Kategorie {
		/***********************************************************************************************
		 * Error-Logging
		 ***********************************************************************************************/
	    ERROR, 
	    
		/***********************************************************************************************
		 * Info-Logging
		 ***********************************************************************************************/
	    INFO;
	}
}